    private void formInternalFrameActivated(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameActivated
        TabelaVendedor tabV = new TabelaVendedor();
        ResultSet ve;
        
        try {
        tabV.Conectar("LDXBC", "root", "root");
        ve = tabV.SelectVendedores();
        while(ve.next()){
            boxVend.addItem(new Vendedor(ve.getString("CDVEND"), ve.getString("DSNOME"), ve.getString("CDTAB"), ve.getString("DTNASC")));
        }
        tabV.Desconectar();
        } catch (ClassNotFoundException | SQLException ex) {
        ex.printStackTrace();
        }
    }//GEN-LAST:event_formInternalFrameActivated
    

