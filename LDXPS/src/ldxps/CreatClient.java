    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String tipo;
        try{
            TabelaCliente tabelaC = new TabelaCliente();
            tabelaC.Conectar("LDXBC", "root", "root");
            if (jTpPessoa.getSelectedItem().equals("Pessoa Física"))
                tipo = "PF";
            else
                tipo = "PJ";
            tabelaC.InsertCliente(_dsnome.getText(), tipo, boxVend.getItemAt(boxVend.getSelectedIndex()).getCdven(), Double.parseDouble(_dslim.getText()));
        }catch(ClassNotFoundException| SQLException cln){
        cln.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

